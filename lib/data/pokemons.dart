    return types[type1];
  }

  getType2Name() {
    return types[type1];
  }

  getEggType1Name() {
    return eggs[egg1];
  }

  gettEggType2Name() {
    return eggs[egg2];
  }

  getColorType1() {
    return typesColors[type1];
  }

  getColorType2() {
    return typesColors[type2];
  }
}

