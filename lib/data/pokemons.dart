class Species {
  int id;
  String number;
  String name;
  String? variantName;
  List<int> baseStats;
  int type1;
  int type2;

  Species(
    this.id,
    this.number,
    this.name,
    this.variantName,
    this.baseStats,
    this.type1,
    this.type2,
  );

  getColorType1() {
    return typesColors[type1];
  }

  getColorType2() {
    return typesColors[type2];
  }
}

int getBase(int pokemonId, int statIndex) {
  return allSpecies[pokemonId]?.baseStats[statIndex] ?? 0;
}

Species getSpeciesByNumber(String number) {
  return allSpecies.entries
      .toList()
      .firstWhere((species) => species.value.number == number)
      .value;
}

